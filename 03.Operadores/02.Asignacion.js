/*
  !============================= Operadores de Asignación =============================!
  El Operador de Asignación simple es igual (=), que asigna el valor de su operando
  derecho a su operando izquierdo.

  Es decir, x = y asigna el valor de y a x.
*/

let x = 20
let y = 40

// Con x = y estamos diciendo que la variable x es igual al valor de y.
x = y

console.log(x)



/*
  - Asignación de Adición (+=)
*/
x += y  // Equivale a: x = x + y
console.log(x)


// 2do Ejemplo:
let num = 5
num += 1  // Equivale a: num = num + 1
console.log(num)



/*
  * Asignación de Resta (-=)
*/
num -= 2  // Equivale a: num = num - 2



/*
  ~ Asignación de Multiplicación (*=)
*/
num *= 4  // Equivale a: num = num * 4



/*
  # Asignación de División (/=)
*/
num /= 2  // Equivale a: num = num / 2



/*
  - Asignación de Residuo (%=)
*/
num %= 2  // Equivale a: num = num % 2



/*
  * Asignación de Exponenciación (**=)
*/
num **= 3  // Equivale a: num = num ** 3
